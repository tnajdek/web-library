const CACHE_NAME = 'zotero-web-library-cache-v1.0.17';

// wipe old caches
self.addEventListener('activate', function(event) {
	event.waitUntil(
		caches.keys().then(cacheNames => {
			return Promise.all(
				cacheNames
				.filter(cacheName => cacheName !== CACHE_NAME && cacheName.startsWith('zotero-web-library-cache'))
				.map(cacheName => caches.delete(cacheName))
			);
		})
	);
});

